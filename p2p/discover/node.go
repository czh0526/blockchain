func logdist(a, b common.Hash) int {
	// 从前向后计算 leading zero的个数。
	// leading zero的个数越多，说明a,b内容越相似，因此距离也越近。
	// 距离 = 总位数 - leading zero count
	lz := 0
	for i := range a {
		x := a[i] ^ b[i]
		if x == 0 {
			lz += 8
		} else {
			lz += lzcount[x]
			break
		}
	}
	return len(a)*8 - lz
}

func distcmp(target, a, b common.Hash) int {
	for i := range target {
		da := a[i] ^ target[i]
		db := b[i] ^ target[i]
		if da > db {
			// a -> taget > b -> target
			return 1
		} else if da < db {
			// a -> target < b -> target
			return -1
		}
		// a -> taget = b -> target; continue loop
	}
	return 0
}

func recoverNodeID(hash, sig []byte) (id NodeID, err error) {
	pubkey, err := secp256k1.RecoverPubkey(hash, sig)
	if err != nil {
		return id, err
	}
	if len(pubkey)-1 != len(id) {
		return id, fmt.Errorf("recovered pubkey has %d bits, want %d bits", len(pubkey)*8, (len(id)+1)*8)
	}
	for i := range id {
		id[i] = pubkey[i+1]
	}
	return id, nil
}
